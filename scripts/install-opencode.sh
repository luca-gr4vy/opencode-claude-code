#!/usr/bin/env bash
set -euo pipefail

MODE='copy'
DRY_RUN='false'
FORCE='false'
WRITE_ENV='true'

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"

SRC_CONFIG="$REPO_DIR/opencode.json"
SRC_TUI="$REPO_DIR/tui.json"
SRC_AGENTS="$REPO_DIR/AGENTS.md"
SRC_COMMANDS_DIR="$REPO_DIR/commands"

DEST_ROOT="$HOME/.config/opencode"
DEST_CONFIG="$DEST_ROOT/opencode.json"
DEST_TUI="$DEST_ROOT/tui.json"
DEST_AGENTS="$DEST_ROOT/AGENTS.md"
DEST_COMMANDS_DIR="$DEST_ROOT/commands"
DEST_ENV="$DEST_ROOT/env.sh"

log() {
  printf '[install-opencode] %s\n' "$*"
}

run_cmd() {
  if [[ "$DRY_RUN" == 'true' ]]; then
    printf '[install-opencode] DRY RUN:'
    for arg in "$@"; do
      printf ' %q' "$arg"
    done
    printf '\n'
    return 0
  fi

  "$@"
}

exists() {
  [[ -e "$1" || -L "$1" ]]
}

backup_path() {
  local path="$1"
  if exists "$path"; then
    local ts
    ts="$(date +%Y%m%d-%H%M%S)"
    local backup="${path}.bak-${ts}"
    run_cmd cp -a "$path" "$backup"
    log "Backed up $path -> $backup"
  fi
}

usage() {
  cat <<'HELP'
Usage: ./scripts/install-opencode.sh [options]

Installs OpenCode config from this repo into ~/.config/opencode.

Options:
  --copy        Copy files/directories (default)
  --symlink     Symlink files/directories from this repo
  --dry-run     Print planned actions without changing files
  --force       Replace existing targets in symlink mode without preserving directory contents
  --no-env      Skip writing ~/.config/opencode/env.sh
  -h, --help    Show this help

Examples:
  ./scripts/install-opencode.sh --dry-run
  ./scripts/install-opencode.sh --copy
  ./scripts/install-opencode.sh --symlink
HELP
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --copy)
      MODE='copy'
      ;;
    --symlink)
      MODE='symlink'
      ;;
    --dry-run)
      DRY_RUN='true'
      ;;
    --force)
      FORCE='true'
      ;;
    --no-env)
      WRITE_ENV='false'
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      log "Unknown option: $1"
      usage
      exit 1
      ;;
  esac
  shift
done

if [[ ! -f "$SRC_CONFIG" || ! -f "$SRC_TUI" || ! -f "$SRC_AGENTS" || ! -d "$SRC_COMMANDS_DIR" ]]; then
  log 'Required source files are missing. Run this script from the repo checkout.'
  exit 1
fi

run_cmd mkdir -p "$DEST_ROOT"

if [[ "$MODE" == 'copy' ]]; then
  if exists "$DEST_CONFIG"; then backup_path "$DEST_CONFIG"; fi
  if exists "$DEST_TUI"; then backup_path "$DEST_TUI"; fi
  if exists "$DEST_AGENTS"; then backup_path "$DEST_AGENTS"; fi

  run_cmd cp "$SRC_CONFIG" "$DEST_CONFIG"
  run_cmd cp "$SRC_TUI" "$DEST_TUI"
  run_cmd cp "$SRC_AGENTS" "$DEST_AGENTS"

  if exists "$DEST_COMMANDS_DIR"; then
    backup_path "$DEST_COMMANDS_DIR"
  fi
  run_cmd mkdir -p "$DEST_COMMANDS_DIR"
  run_cmd rsync -a --delete "$SRC_COMMANDS_DIR/" "$DEST_COMMANDS_DIR/"

  log 'Installed OpenCode config files (copy mode).'
else
  if [[ "$FORCE" == 'true' ]]; then
    run_cmd rm -rf "$DEST_CONFIG" "$DEST_TUI" "$DEST_AGENTS" "$DEST_COMMANDS_DIR"
  else
    if exists "$DEST_CONFIG" && [[ ! -L "$DEST_CONFIG" ]]; then
      backup_path "$DEST_CONFIG"
      run_cmd rm -f "$DEST_CONFIG"
    fi
    if exists "$DEST_TUI" && [[ ! -L "$DEST_TUI" ]]; then
      backup_path "$DEST_TUI"
      run_cmd rm -f "$DEST_TUI"
    fi
    if exists "$DEST_AGENTS" && [[ ! -L "$DEST_AGENTS" ]]; then
      backup_path "$DEST_AGENTS"
      run_cmd rm -f "$DEST_AGENTS"
    fi
    if exists "$DEST_COMMANDS_DIR" && [[ ! -L "$DEST_COMMANDS_DIR" ]]; then
      backup_path "$DEST_COMMANDS_DIR"
      run_cmd rm -rf "$DEST_COMMANDS_DIR"
    fi
  fi

  run_cmd ln -sfn "$SRC_CONFIG" "$DEST_CONFIG"
  run_cmd ln -sfn "$SRC_TUI" "$DEST_TUI"
  run_cmd ln -sfn "$SRC_AGENTS" "$DEST_AGENTS"
  run_cmd ln -sfn "$SRC_COMMANDS_DIR" "$DEST_COMMANDS_DIR"

  log 'Installed OpenCode config files (symlink mode).'
fi

if [[ "$WRITE_ENV" == 'true' ]]; then
  local_tmp="$(mktemp)"
  cat > "$local_tmp" <<ENV
# Generated by install-opencode.sh
export OPENCODE_CONFIG="$DEST_CONFIG"
export OPENCODE_CONFIG_DIR="$DEST_ROOT"
ENV
  if exists "$DEST_ENV"; then
    backup_path "$DEST_ENV"
  fi
  run_cmd cp "$local_tmp" "$DEST_ENV"
  run_cmd chmod 600 "$DEST_ENV"
  rm -f "$local_tmp"
  log "Wrote environment snippet: $DEST_ENV"
  log "Load it with: source $DEST_ENV"
else
  log 'Skipping env snippet write (--no-env).'
fi

log 'Done.'
